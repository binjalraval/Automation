package Homework3;

public class Arithmeticoperator {
    public static void main(String[] args) {
        int a=5;
        int b=10;
        boolean c=true;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(!c);

    }
}
